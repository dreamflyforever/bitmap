static int x, y;

void bitmap_set(int prio)
{
	x = x | (1 << (prio & 0x7));
	y = y | (1 << (prio >> 3));
}

/*TODO: when y is the some, it cant just clear the y*/
void bitmap_clear(int prio)
{
	
	x = x & (~(1 << (prio & 0x7)));
	y = y & (~(1 << (prio >> 3)));
}

int bitmap_get()
{
	int a, b, prio;
	a = bitmap[x];
	b = bitmap[y];
	prio = a + b*8;
	return prio;
}

