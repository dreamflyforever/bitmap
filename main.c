static unsigned char y, c[8] = {0};

void bitmap_set(int prio)
{
	unsigned char a, b;
	a = prio & 0x7;
	b = prio >> 3;
	c[b] |= 1 << a;
	y = y | 1 << b;
}

int bitmap_get()
{
	unsigned char a, b, prio;
	b = bitmap[y];
	a = bitmap[c[b]];
	prio = b*8 + a;
	return prio;
}

void bitmap_clear(int prio)
{
	unsigned char a, b;
	a = prio & 0x7;
	b = prio >> 3;
	c[b] &= ~(1 << a);
	if (c[b] == 0)
		y = y & ~(1 << b);
}

int main()
{
	int prio = 11;
	bitmap_set(prio);
	bitmap_set(1);
	bitmap_clear(1);
	bitmap_set(2);
	prio = bitmap_get();
	printf("prio: %d\n", prio);
	return 0;
}
